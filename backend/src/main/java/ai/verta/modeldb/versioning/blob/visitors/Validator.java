  public Validator(AutogenBlobDiff autogenBlobDiff) throws ModelDBException {
    if (autogenBlobDiff.getLocation().isEmpty()) {
      throw new ModelDBException("Blob diff location is empty", Status.Code.INVALID_ARGUMENT);
    }
    autogenBlobDiff.preVisitDeep(this);
    checkStatus(autogenBlobDiff.getStatus());
  }

  private void checkStatus(AutogenDiffStatusEnumDiffStatus status) throws ModelDBException {
    checkStatus(status, () -> {}, () -> {}, () -> {});
  }

  private void checkStatus(
      AutogenDiffStatusEnumDiffStatus diffStatus,
      BlobContainerBase deleted,
      BlobContainerBase added,
      BlobContainerBase modified)
      throws ModelDBException {
    switch (diffStatus.toProto()) {
      case DELETED:
        deleted.validate();
        break;
      case ADDED:
        added.validate();
        break;
      case MODIFIED:
        modified.validate();
        break;
      default:
        throw new ModelDBException("Unknown type", Status.Code.INVALID_ARGUMENT);
    }
  }

